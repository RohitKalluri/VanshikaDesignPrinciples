package org.epamAssignment;

public class functions {
    public int add(int a,int b)
    {
        System.out.println(a+" and "+b+" added!");
        return a+b;
    }
    public int subtract(int a ,int b)
    {
        System.out.println(a+" and "+b+" subtracted!");
        return a-b;
    }
    public int multiply(int a ,int b)
    {
        System.out.println(a+" and "+b+" multiplied!");
        return a*b;
    }
    public int divide(int a,int b)
    {
        System.out.println(a+" and "+b+" divided!");
        return a/b;
    }
}
